int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int is_equal;

	is_equal = 1;
	while (*s1 && n > 0)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		n--;
	}
	return (0);
}
