#include "sudoku.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		ft_atoi(char c)
{
	if (c >= '0' && c <= '9')
		return (c - 48);
	return (0);
}

char	ft_itoa(int i)
{
	if (i >= 0 && i <= 9)
		return (i + 48);
	return ('0');
}
